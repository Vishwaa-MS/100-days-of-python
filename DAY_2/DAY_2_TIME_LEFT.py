#Remaining time 
age=int(input('Enter Your Age: '))
years_remaining=19-age
no_of_days_remaining=years_remaining*365
no_of_weeks_remaining=years_remaining*52
no_of_months_remaining=years_remaining*12
print(f'You have {no_of_days_remaining} days,{no_of_weeks_remaining} weeks,and {no_of_months_remaining} months left')